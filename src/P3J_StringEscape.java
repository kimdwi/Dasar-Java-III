public class P3J_StringEscape {
    public static void main(String[] args) {
        //Menggunakan Escape Character
        String str1 = "POLBENG";
        String str2 = "polbeng";

        //Menampilkan Hasil
        System.out.println(str1+ "\n" +str2);
        String str3 = "RPL";
        System.out.println(str1+ "\t" +str3);
    }
}
